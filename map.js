//examples of map in JavaScript

let fruits = ["apple", "banana", "mango"];

let result = fruits.map(fruit => fruit.toUpperCase());

console.log(result); // ["APPLE", "BANANA", "MANGO"]


let scores = [70, 82, 65, 90];

let updatedScores = scores.map(score => score + 5);

console.log(updatedScores); // [75, 87, 70, 95]

